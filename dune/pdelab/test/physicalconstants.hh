#ifndef DUNE_PDELAB_PHYSICALCONSTANTS_HH
#define DUNE_PDELAB_PHYSICALCONSTANTS_HH

//
//  PHYSICAL CONSTANTS
//

const double pi = 3.1415926535897932384626433832795;

// free space velocity of light
const double c0 = 299792458.0;

// vacuum permeability
const double mu0 = 4e-7 * pi;

// vacuum permittivity
const double eps0 = 1/(mu0*c0*c0);

#endif // DUNE_PDELAB_PHYSICALCONSTANTS_HH
